@function_tool
def get_attractions(city: str, interests: str = "综合") -> str:
    """获取指定城市的景点推荐，支持按兴趣分类筛选。

    Args:
        city: 目标城市名称。
        interests: 兴趣标签，可选值为"文化"、"自然"、"美食"、"综合"，默认为"综合"。

    Returns:
        格式化的景点推荐信息字符串。
    """
    city_data = _ATTRACTIONS.get(city, {})
    attractions = city_data.get(interests) or city_data.get("综合") or _DEFAULT_ATTRACTIONS

    lines = [
        f"🎯 {city} 热门景点推荐（{interests}类）",
        "=" * 35,
    ]
    for i, spot in enumerate(attractions[:5], 1):
        lines.append(
            f"\n{i}. 📍 {spot['name']}\n"
            f"   📝 简介：{spot['desc']}\n"
            f"   ⏱️  游览时间：{spot['duration']}\n"
            f"   🎫 门票：{spot['ticket']}\n"
            f"   💡 小贴士：{spot['tip']}"
        )
    lines.append(
        "\n🗺️ 建议根据个人体力合理安排每日行程，不要贪多，留出自由探索时间！"
    )
    return "\n".join(lines)
